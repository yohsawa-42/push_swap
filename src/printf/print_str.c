#include "push_swap.h"

int	print_str(char *s)
{
	int		i;

	if (!s)
	{
		if ((write (2, "(null)", 6)) < 0)
			return (-1);
		return (6);
	}
	i = 0;
	while (s[i] != '\0')
	{
		if (write(2, &s[i], 1) < 0)
			return (-1);
		i++;
	}
	return (i);
}
